package jsoft.objects;

public class SkillObject {
    private int skill_id;
    private String skill_name;
    private String skill_created_date;
    private int skill_delete;
    private int skill_enable;
    private String skill_last_modified;
    private int skill_author_id;
    private int skill_actions;
    
    
	public int getSkill_id() {
		return skill_id;
	}
	public void setSkill_id(int skill_id) {
		this.skill_id = skill_id;
	}
	public String getSkill_name() {
		return skill_name;
	}
	public void setSkill_name(String skill_name) {
		this.skill_name = skill_name;
	}
	public String getSkill_created_date() {
		return skill_created_date;
	}
	public void setSkill_created_date(String skill_created_date) {
		this.skill_created_date = skill_created_date;
	}
	public int getSkill_delete() {
		return skill_delete;
	}
	public void setSkill_delete(int skill_delete) {
		this.skill_delete = skill_delete;
	}
	public int getSkill_enable() {
		return skill_enable;
	}
	public void setSkill_enable(int skill_enable) {
		this.skill_enable = skill_enable;
	}
	public String getSkill_last_modified() {
		return skill_last_modified;
	}
	public void setSkill_last_modified(String skill_last_modified) {
		this.skill_last_modified = skill_last_modified;
	}
	public int getSkill_author_id() {
		return skill_author_id;
	}
	public void setSkill_author_id(int skill_author_id) {
		this.skill_author_id = skill_author_id;
	}
	public int getSkill_actions() {
		return skill_actions;
	}
	public void setSkill_actions(int skill_actions) {
		this.skill_actions = skill_actions;
	}


    // Các phương thức getter và setter cho các thuộc tính
   
}
