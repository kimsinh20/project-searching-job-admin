package jsoft.objects;

public class LocationObject {
	 private int locationId;
	    private String locationDetail;
	    private String locationNotes;
	    private String locationProvinceCode;
	    private String locationDistrictCode;
	    private String locationWardCode;
	    private boolean locationIsDefault;
	    private String locationCreatedDate;
		public int getLocationId() {
			return locationId;
		}
		public void setLocationId(int locationId) {
			this.locationId = locationId;
		}
		public String getLocationDetail() {
			return locationDetail;
		}
		public void setLocationDetail(String locationDetail) {
			this.locationDetail = locationDetail;
		}
		public String getLocationNotes() {
			return locationNotes;
		}
		public void setLocationNotes(String locationNotes) {
			this.locationNotes = locationNotes;
		}
		public String getLocationProvinceCode() {
			return locationProvinceCode;
		}
		public void setLocationProvinceCode(String locationProvinceCode) {
			this.locationProvinceCode = locationProvinceCode;
		}
		public String getLocationDistrictCode() {
			return locationDistrictCode;
		}
		public void setLocationDistrictCode(String locationDistrictCode) {
			this.locationDistrictCode = locationDistrictCode;
		}
		public String getLocationWardCode() {
			return locationWardCode;
		}
		public void setLocationWardCode(String locationWardCode) {
			this.locationWardCode = locationWardCode;
		}
		public boolean isLocationIsDefault() {
			return locationIsDefault;
		}
		public void setLocationIsDefault(boolean locationIsDefault) {
			this.locationIsDefault = locationIsDefault;
		}
		public String getLocationCreatedDate() {
			return locationCreatedDate;
		}
		public void setLocationCreatedDate(String locationCreatedDate) {
			this.locationCreatedDate = locationCreatedDate;
		}
	    
	    
}
